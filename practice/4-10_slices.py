animals = ['cat', 'dog', 'bird', 'fish', 'frog', 'mouse', 'iguana']

print("The first three items in the list are:")
first_animals = animals[:3]
print(first_animals)

print("The items in the middle of the list are:")
middle_animals = animals[2:-2]
print(middle_animals)

print("The last three items in the list are:")
last_animals = animals[-3:]
print(last_animals)